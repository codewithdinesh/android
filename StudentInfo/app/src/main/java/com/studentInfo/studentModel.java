package com.studentInfo;

public class studentModel {

    String str_name, str_phone, str_branch, str_year, str_age, str_address, str_roll_no;

    public studentModel() {
    }

    public String getStr_name() {
        return str_name;
    }

    public void setStr_name(String str_name) {
        this.str_name = str_name;
    }

    public String getStr_phone() {
        return str_phone;
    }

    public void setStr_phone(String str_phone) {
        this.str_phone = str_phone;
    }

    public String getStr_branch() {
        return str_branch;
    }

    public void setStr_branch(String str_branch) {
        this.str_branch = str_branch;
    }

    public String getStr_year() {
        return str_year;
    }

    public void setStr_year(String str_year) {
        this.str_year = str_year;
    }

    public String getStr_age() {
        return str_age;
    }

    public void setStr_age(String str_age) {
        this.str_age = str_age;
    }

    public String getStr_address() {
        return str_address;
    }

    public void setStr_address(String str_address) {
        this.str_address = str_address;
    }

    public String getStr_roll_no() {
        return str_roll_no;
    }

    public void setStr_roll_no(String str_roll_no) {
        this.str_roll_no = str_roll_no;
    }
}
